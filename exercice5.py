# Exercice 5 : Simulation d’un Distributeur de Billets (Conditions, Boucles & 
# Fonctions) 
# Énoncé : 
# 1. Demandez à l'utilisateur de saisir le **montant à retirer**. 
# 2. Vérifiez que le montant est un **multiple de 10** (ex: 20€, 50€, 100€ sont valides, mais 
# 27€ est invalide). 
# 3. Vérifiez que le **montant ne dépasse pas 500€** (limite de retrait). 
# 4. Si le montant est valide, affichez **le nombre de billets** de **50€, 20€ et 10€** 
# nécessaires pour atteindre ce montant. 
# 5. Répétez la demande tant que l'utilisateur ne rentre pas un montant valide. 